public class Pattern {
    public static void main(String[] args) {
        System.out.println("    *    ");
        System.out.println("  * * *  ");
        System.out.println("* * * * *");
        System.out.println("  * * *  ");
        System.out.println("    *    ");

        System.out.println("Conversion Example: " + (int) (1.0 - 0.1 - 0.1 - 0.1));
        System.out.println("Fraction in int: " + (1.0 - 0.1 - 0.1 - 0.1));
        System.out.println("Math Power: " + Math.pow(4, 0.5));
    }
}
